// In the script below I make use of the filter() and end() functions.  I select only the
// odd li elements and apply a background color of blue to them.  Then I use the end() function
// to pop the filtered result off the stack and select the original li tag once again.  From there
// I apply a css font color of red to all the li elements.
$(document).ready(function() {

  $('li')
    .filter(':odd').css('background-color', 'blue')
    .end()
    .css('color', 'red')

});
